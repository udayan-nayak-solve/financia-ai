"""
Enhanced Census JSON to CSV converter with meaningful column names.
Converts Census Bureau API responses to CSV with proper column naming and data types.
"""

import json
import pandas as pd
import sys
from pathlib import Path


# Configuration variables
DATA_YEAR = 2023  # Change this value to set the year for all data rows

# Census Bureau variable code mappings
CENSUS_VARIABLES = {
    'B01003_001E': 'total_population',
    'B23025_003E': 'civilian_labor_force',
    'B23025_005E': 'unemployed',
    'B01002_001E': 'median_age',
    'B19013_001E': 'median_household_income',

    
    'B25077_001E': 'median_home_value', 
    'B08303_001E': 'total_commuters',
    'B08301_010E': 'public_transportation_users',
    'B25003_002E': 'owner_occupied_housing',
    'B25003_003E': 'renter_occupied_housing',
    'B15003_022E': 'bachelors_degree_holders',
    'B15003_023E': 'masters_degree_holders',
    'B25024_002E': 'single_family_homes',
    'B25024_003E': 'mobile_homes',
    'B08301_001E': 'total_workers',
    'B08301_021E': 'work_from_home',
    'state': 'state_code',
    'county': 'county_code', 
    'tract': 'tract_code'
}


def convert_census_json_enhanced(json_data=None, csv_file_path="census_data.csv"):
    """
    Convert Census JSON to CSV with enhanced column names and data processing.
    
    Args:
        json_data: JSON data as string or list (if None, uses built-in sample)
        csv_file_path: Path to output CSV file
    """
    
    print(f"Converting JSON data to {csv_file_path}")
    
    # Use provided JSON data or default sample
    if json_data is None:
        # Default sample JSON data - replace this with your actual JSON
        json_string = '''[
            [
                "B23025_003E",
                "B23025_005E",
                "B01003_001E",
                "B01002_001E",
                "state",
                "county",
                "tract"
            ],
            [
                "833",
                "45",
                "1848",
                "41.5",
                "20",
                "001",
                "952600"
            ],
            [
                "995",
                "33",
                "1883",
                "44.9",
                "20",
                "001",
                "952700"
            ]
        ]'''
        data = json.loads(json_string)
    elif isinstance(json_data, str):
        # Parse JSON string
        data = json.loads(json_data)
    else:
        # Use data directly
        data = json_data
    
    if not data or len(data) < 2:
        print("Error: JSON must contain at least headers and one data row")
        return
    
    # Extract headers and data
    headers = data[0]
    rows = data[1:]
    
    print(f"Original headers: {headers}")
    print(f"Data rows: {len(rows)}")
    
    # Create DataFrame
    df = pd.DataFrame(rows, columns=headers)
    
    # Rename columns using meaningful names
    new_column_names = {}
    for col in df.columns:
        if col in CENSUS_VARIABLES:
            new_name = CENSUS_VARIABLES[col]
            new_column_names[col] = new_name
            print(f"Renaming '{col}' → '{new_name}'")
    
    df = df.rename(columns=new_column_names)
    
    # Convert data types
    numeric_columns = [
        'civilian_labor_force', 'unemployed', 'total_population', 'median_age',
        'median_household_income', 'median_home_value', 'total_commuters',
        'public_transportation_users', 'owner_occupied_housing', 'renter_occupied_housing',
        'bachelors_degree_holders', 'masters_degree_holders', 'single_family_homes',
        'mobile_homes', 'total_workers', 'work_from_home'
    ]
    
    for col in numeric_columns:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors='coerce')
            print(f"Converted '{col}' to numeric")
    
    # Create census tract identifier
    if all(col in df.columns for col in ['state_code', 'county_code', 'tract_code']):
        df['census_tract'] = (
            df['state_code'].astype(str).str.zfill(2) + 
            df['county_code'].astype(str).str.zfill(3) + 
            df['tract_code'].astype(str).str.zfill(6)
        )
        print("Created combined 'census_tract' identifier")
    
    # Calculate derived metrics
    if 'civilian_labor_force' in df.columns and 'unemployed' in df.columns:
        df['unemployment_rate'] = (df['unemployed'] / df['civilian_labor_force'] * 100).round(2)
        print("Calculated unemployment_rate")
    
    if 'owner_occupied_housing' in df.columns and 'renter_occupied_housing' in df.columns:
        total_housing = df['owner_occupied_housing'] + df['renter_occupied_housing']
        df['homeownership_rate'] = (df['owner_occupied_housing'] / total_housing * 100).round(2)
        print("Calculated homeownership_rate")
    
    # Add year column
    df['year'] = DATA_YEAR
    print(f"Added year column with value: {DATA_YEAR}")
    
    # Create output directory if needed
    output_path = Path(csv_file_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Save to CSV
    df.to_csv(csv_file_path, index=False)
    
    print(f"\n✅ Successfully created enhanced CSV: {csv_file_path}")
    print(f"Final columns: {list(df.columns)}")
    print(f"Shape: {df.shape}")
    
    # Show data preview
    print("\n📊 Data Preview:")
    print(df.head(3).to_string(index=False))
    
    # Show summary statistics for numeric columns
    numeric_cols = df.select_dtypes(include=['number']).columns
    if len(numeric_cols) > 0:
        print(f"\n📈 Summary Statistics:")
        print(df[numeric_cols].describe().round(2))


